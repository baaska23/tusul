package com.example.mdbspringboot.model;

public class Injection {

    private int drop;
    private int blood_vessel;
    private int muscle;
    private int inside_skin;
    private int under_skin;

    public int getDrop() {
        return drop;
    }

    public void setDrop(int drop) {
        this.drop = drop;
    }

    public int getBlood_vessel() {
        return blood_vessel;
    }

    public void setBlood_vessel(int blood_vessel) {
        this.blood_vessel = blood_vessel;
    }

    public int getMuscle() {
        return muscle;
    }

    public void setMuscle(int muscle) {
        this.muscle = muscle;
    }

    public int getInside_skin() {
        return inside_skin;
    }

    public void setInside_skin(int inside_skin) {
        this.inside_skin = inside_skin;
    }

    public int getUnder_skin() {
        return under_skin;
    }

    public void setUnder_skin(int under_skin) {
        this.under_skin = under_skin;
    }
}
